// Role: verifies Stage 4 (spec Section 3): governance references are load-bearing comments, not
//   decoration. Parses every `// Governs: claim-N: <sentence>` line under api/, periphery/, vault/,
//   and build/, asserts each named claim id resolves to a real claim in
//   kernel/governance/corpora/knowledge-game-data.js, and asserts coverage: every claim id 1 through
//   20 is referenced by at least one file. Also regenerates docs/blast-radius.md from the same parse
//   and fails if the committed copy has drifted from a fresh regeneration.
// Contract: `node build/check-references.mjs` exits non-zero on any unresolved reference, any
//   uncovered claim id, or a stale docs/blast-radius.md, naming what failed. Exports
//   collectReferences() and renderBlastRadius() for build/found-community.mjs's sibling tooling and
//   any future caller that needs the same parse without re-implementing it.
// Invariant: a reference to a nonexistent claim id is a check failure, not a warning; the coverage
//   assertion walks claim ids 1 through 20 by number, so a claim referenced by zero files fails by its
//   own id, never silently passing because some other claim happens to be over-referenced.
"use strict";
import { readFileSync, readdirSync, statSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join, relative, resolve, extname } from "node:path";

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");
let fails = 0;
const ok = (c, m) => { console.log(`${c ? "  ok  " : " FAIL "} ${m}`); if (!c) fails++; };
const H = "=".repeat(80);
console.log(H); console.log("CHECK-REFERENCES: governance references resolve, and every claim is covered"); console.log(H);

const SCAN_DIRS = ["api", "periphery", "vault", "build"];
const GOVERNS_RE = /^\s*\/\/\s*Governs:\s*claim-(\d+):\s*(.+?)\s*$/;

function walk(dir, out) {
  for (const name of readdirSync(dir)) {
    const p = join(dir, name);
    const st = statSync(p);
    if (st.isDirectory()) walk(p, out);
    else if (extname(name) === ".js" || extname(name) === ".mjs") out.push(p);
  }
}

const CONTINUATION_RE = /^\s*\/\/\s{3}(.+?)\s*$/;

export function collectReferences(root) {
  const files = [];
  for (const d of SCAN_DIRS) walk(join(root, d), files);
  const refs = []; // { claimId, file, sentence }
  for (const f of files) {
    const text = readFileSync(f, "utf8");
    const lines = text.split("\n");
    for (let i = 0; i < lines.length; i++) {
      const m = lines[i].match(GOVERNS_RE);
      if (!m) continue;
      const parts = [m[2]];
      let j = i + 1;
      while (j < lines.length) {
        const cont = lines[j].match(CONTINUATION_RE);
        if (!cont || GOVERNS_RE.test(lines[j])) break;
        parts.push(cont[1]);
        j++;
      }
      refs.push({ claimId: Number(m[1]), file: relative(root, f), sentence: parts.join(" ") });
    }
  }
  return refs;
}

export function renderBlastRadius(refs, claimStatements, checkerMap) {
  const byClaim = new Map();
  for (const r of refs) {
    if (!byClaim.has(r.claimId)) byClaim.set(r.claimId, []);
    byClaim.get(r.claimId).push(r);
  }
  const lines = [];
  lines.push("# Blast radius");
  lines.push("");
  lines.push("Generated by `node build/check-references.mjs` from every `// Governs: claim-N` comment in");
  lines.push("api/, periphery/, vault/, and build/. A change to a claim's statement or grade touches exactly");
  lines.push("the files and checks listed under it; nothing here is hand-maintained.");
  lines.push("");
  for (let n = 1; n <= 20; n++) {
    const statement = claimStatements[n] || "(unknown claim)";
    const list = (byClaim.get(n) || []).slice().sort((a, b) => a.file.localeCompare(b.file));
    const checkers = checkerMap[n] || [];
    lines.push(`## claim-${n}`);
    lines.push("");
    lines.push(statement);
    lines.push("");
    lines.push(`Referenced by ${list.length} file(s):`);
    for (const r of list) lines.push(`- \`${r.file}\`: ${r.sentence}`);
    lines.push("");
    lines.push(`Grounded by ${checkers.length} check(s): ${checkers.map((c) => `\`${c}\``).join(", ") || "(none)"}`);
    lines.push("");
  }
  return lines.join("\n");
}

async function loadClaimData(root) {
  const require = (await import("node:module")).createRequire(import.meta.url);
  const { STORE } = require(join(root, "kernel", "governance", "corpora", "knowledge-game-data.js"));
  const statements = {};
  const checkerMap = {};
  const realIds = new Set();
  for (const c of STORE.claims) {
    const n = Number(c.ref.replace("claim-", ""));
    realIds.add(n);
    statements[n] = c.statement;
    checkerMap[n] = (c.checking_records || []).map((r) => r.checker_id);
  }
  return { statements, checkerMap, realIds };
}

const { statements, checkerMap, realIds } = await loadClaimData(ROOT);
const refs = collectReferences(ROOT);

console.log(`\n[1] every governance reference resolves to a real claim id (${refs.length} references parsed)`);
for (const r of refs) {
  ok(realIds.has(r.claimId), `${r.file} references claim-${r.claimId}, which exists in the governance kernel`);
}

console.log("\n[2] coverage: every claim id 1 through 20 is referenced by at least one file");
const covered = new Set(refs.map((r) => r.claimId));
for (let n = 1; n <= 20; n++) {
  const list = refs.filter((r) => r.claimId === n);
  ok(list.length > 0, `claim-${n} is referenced by at least one file (got ${list.length})`);
}

console.log("\n[3] docs/blast-radius.md is a fresh regeneration, not a stale committed copy");
const fresh = renderBlastRadius(refs, statements, checkerMap);
const blastPath = join(ROOT, "docs", "blast-radius.md");
let committed = null;
try { committed = readFileSync(blastPath, "utf8"); } catch { committed = null; }
if (committed === null) {
  writeFileSync(blastPath, fresh);
  ok(true, "docs/blast-radius.md did not exist; wrote it fresh");
} else {
  ok(committed === fresh, "docs/blast-radius.md matches a fresh regeneration byte-for-byte");
  if (committed !== fresh) writeFileSync(blastPath, fresh);
}

console.log("\n" + H);
if (fails === 0) console.log(`verified: ${refs.length} governance references all resolve, all 20 claims are covered, and docs/blast-radius.md is current.`);
console.log(fails === 0 ? "check-references: OK" : `check-references: ${fails} FAILURE(S)`);
console.log(H);
process.exit(fails === 0 ? 0 : 1);
